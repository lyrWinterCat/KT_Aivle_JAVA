package com.example.chap01;

public class IntegerValues {
    public static void main(String[] args) {
        byte a = 100;
        short b = 101;
        int c = 102;
        long d = 103;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        
    }
}
